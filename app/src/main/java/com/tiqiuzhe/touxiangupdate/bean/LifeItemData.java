package com.tiqiuzhe.touxiangupdate.bean;


public class LifeItemData {
	private int icon;
	private String name;
	public LifeItemData(int icon, String name) {
		super();
		this.icon = icon;
		this.name = name;
	}
	public int getIcon() {
		return icon;
	}
	public void setIcon(int icon) {
		this.icon = icon;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	

}
